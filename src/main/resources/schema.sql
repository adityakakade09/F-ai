-- FraudGuard AI Database Schema
-- Compatible with Oracle Database 21c and H2 (Oracle Mode)

-- 1. ROLES Table
CREATE TABLE ROLES (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(50) NOT NULL UNIQUE
);

-- 2. USERS Table
CREATE TABLE USERS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR2(100) NOT NULL UNIQUE,
    password VARCHAR2(255) NOT NULL,
    email VARCHAR2(100) NOT NULL UNIQUE,
    full_name VARCHAR2(150) NOT NULL,
    active NUMBER(1) DEFAULT 1 NOT NULL,
    locked NUMBER(1) DEFAULT 0 NOT NULL,
    failed_login_attempts NUMBER DEFAULT 0 NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

-- 3. USER_ROLES Join Table
CREATE TABLE USER_ROLES (
    user_id NUMBER NOT NULL,
    role_id NUMBER NOT NULL,
    PRIMARY KEY (user_id, role_id),
    CONSTRAINT fk_ur_user FOREIGN KEY (user_id) REFERENCES USERS(id) ON DELETE CASCADE,
    CONSTRAINT fk_ur_role FOREIGN KEY (role_id) REFERENCES ROLES(id) ON DELETE CASCADE
);

-- 4. TRANSACTIONS Table
CREATE TABLE TRANSACTIONS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    account_number VARCHAR2(50) NOT NULL,
    amount NUMBER(15,2) NOT NULL,
    transaction_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    location VARCHAR2(150) NOT NULL,
    device VARCHAR2(150) NOT NULL,
    status VARCHAR2(50) NOT NULL
);

-- 5. FRAUD_RESULTS Table
CREATE TABLE FRAUD_RESULTS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    transaction_id NUMBER NOT NULL UNIQUE,
    fraud_score NUMBER(5,2) NOT NULL,
    risk_level VARCHAR2(50) NOT NULL,
    fraud_reason VARCHAR2(1000) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_fr_tx FOREIGN KEY (transaction_id) REFERENCES TRANSACTIONS(id) ON DELETE CASCADE
);

-- 6. AUDIT_LOGS Table
CREATE TABLE AUDIT_LOGS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR2(100) NOT NULL,
    action VARCHAR2(255) NOT NULL,
    timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    ip_address VARCHAR2(50) NOT NULL,
    browser VARCHAR2(150) NOT NULL,
    module VARCHAR2(100) NOT NULL
);

-- 7. LOGIN_SESSIONS Table
CREATE TABLE LOGIN_SESSIONS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR2(100) NOT NULL,
    login_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    logout_time TIMESTAMP NULL,
    ip_address VARCHAR2(50) NOT NULL,
    browser VARCHAR2(150) NOT NULL,
    token VARCHAR2(1000) NOT NULL
);

-- 8. NOTIFICATIONS Table
CREATE TABLE NOTIFICATIONS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    message VARCHAR2(1000) NOT NULL,
    read_status NUMBER(1) DEFAULT 0 NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    target_role VARCHAR2(50) NOT NULL
);

-- 9. PASSWORD_RESET_TOKENS Table
CREATE TABLE PASSWORD_RESET_TOKENS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    token VARCHAR2(255) NOT NULL UNIQUE,
    user_id NUMBER NOT NULL,
    expiry_date TIMESTAMP NOT NULL,
    CONSTRAINT fk_prt_user FOREIGN KEY (user_id) REFERENCES USERS(id) ON DELETE CASCADE
);

-- 10. EMAIL_VERIFICATION_TOKENS Table
CREATE TABLE EMAIL_VERIFICATION_TOKENS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    token VARCHAR2(255) NOT NULL UNIQUE,
    user_id NUMBER NOT NULL,
    expiry_date TIMESTAMP NOT NULL,
    CONSTRAINT fk_evt_user FOREIGN KEY (user_id) REFERENCES USERS(id) ON DELETE CASCADE
);

-- CREATE INDEXES for Performance
CREATE INDEX idx_users_username ON USERS(username);
CREATE INDEX idx_tx_acc ON TRANSACTIONS(account_number);
CREATE INDEX idx_tx_date ON TRANSACTIONS(transaction_date);
CREATE INDEX idx_fr_level ON FRAUD_RESULTS(risk_level);
CREATE INDEX idx_audit_user ON AUDIT_LOGS(username);
CREATE INDEX idx_session_user ON LOGIN_SESSIONS(username);
